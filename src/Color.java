
public class Color {
	private double r;
	private double g;
	private double b;
	
	public Color(double color0, double color1, double color2){
		this.r = color0;
		this.g = color1;
		this.b = color2;
	}

	public double getR() {
		return r;
	}

	public void setR(double r) {
		this.r = r;
	}

	public double getG() {
		return g;
	}

	public void setG(double g) {
		this.g = g;
	}

	public double getB() {
		return b;
	}

	public void setB(double b) {
		this.b = b;
	}

}
